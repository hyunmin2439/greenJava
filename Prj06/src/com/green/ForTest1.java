package com.green;

public class ForTest1 {
	
	public static void main(String[] args) {
		
		System.out.println("아이유");
		System.out.println("아이유");
		System.out.println("아이유");
		System.out.println("아이유");
		System.out.println("아이유");
		System.out.println("-----------------------------");
		
		for(int i=1;i<=5;i++) {
			System.out.println(i + ".아이유");			
		}
		System.out.println("-----------------------------");
		
		for(int i=0;i < 5;i++) {
			System.out.println(i + ".아이유");		
		}
		System.out.println("-----------------------------");
		
		for(int i=5;i >= 1;i--) {
			System.out.println(i + ".아이유");		
		}
		System.out.println("-----------------------------");
		
		for(int i=1;i <= 9 ; i += 2) {
			System.out.println(i + ".아이유");		
		}
		System.out.println("-----------------------------");
		
		
				
	}
	
	
}




